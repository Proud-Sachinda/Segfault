package com.Objects;

public class ExportItem {
    String coursecode ;
    String topicname ;
    String date ;
    String yos ;
    String degree ;
    String faculties ;
    String internalexaminer ;
    String externalexaminer ;
    String material ;
    String time ;
    String mark ;
    String instructions ;

    public String getCoursecode() {
        return coursecode;
    }

    public void setCoursecode(String coursecode) {
        this.coursecode = coursecode;
    }

    public String getTopicname() {
        return topicname;
    }

    public void setTopicname(String topicname) {
        this.topicname = topicname;
    }

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }

    public String getYos() {
        return yos;
    }

    public void setYos(String yos) {
        this.yos = yos;
    }

    public String getDegree() {
        return degree;
    }

    public void setDegree(String degree) {
        this.degree = degree;
    }

    public String getFaculties() {
        return faculties;
    }

    public void setFaculties(String faculties) {
        this.faculties = faculties;
    }

    public String getInternalexaminer() {
        return internalexaminer;
    }

    public void setInternalexaminer(String internalexaminer) {
        this.internalexaminer = internalexaminer;
    }

    public String getExternalexaminer() {
        return externalexaminer;
    }

    public void setExternalexaminer(String externalexaminer) {
        this.externalexaminer = externalexaminer;
    }

    public String getMaterial() {
        return material;
    }

    public void setMaterial(String material) {
        this.material = material;
    }

    public String getTime() {
        return time;
    }

    public void setTime(String time) {
        this.time = time;
    }

    public String getMark() {
        return mark;
    }

    public void setMark(String mark) {
        this.mark = mark;
    }

    public String getInstructions() {
        return instructions;
    }

    public void setInstructions(String instructions) {
        this.instructions = instructions;
    }
}
